#include "libft.h"

ssize_t	 ft_strchr_i(const char *s, char c)
{
	size_t	i;

	i = 0;
	while (s && s[i] && s[i] != c)
		i++;
	return (-1 + ((int)c + 1) * (s[i] == c));
}
